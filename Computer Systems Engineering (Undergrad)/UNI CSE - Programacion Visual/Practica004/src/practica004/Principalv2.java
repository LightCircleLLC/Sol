    private boolean validarMatricula(String matricula) {
        return matricula.matches("\\d{6}");
    }

    private boolean validarNombre(String nombre) {
        return !nombre.isEmpty();
    }

public static void agregarEnArchivo(JTextArea textArea, String nombreArchivo) {
    try {
        String texto = textArea.getText(); // Obtén el texto del JTextArea
        if (!texto.isEmpty()) {
            // Agrega un salto de línea al final del texto si no está vacío
            if (!texto.endsWith("\n")) {
                texto += "\n";
            }

            BufferedWriter writer = new BufferedWriter(new FileWriter(nombreArchivo, true)); // Abre en modo de adición (true)
            writer.write(texto);
            writer.close();
            System.out.println("Texto agregado en " + nombreArchivo);
        }
    } catch (IOException e) {
        e.printStackTrace();
    }
}

    public static String leerArchivo(String nombreArchivo) {
        StringBuilder contenido = new StringBuilder();
        try {
            BufferedReader br = new BufferedReader(new FileReader(nombreArchivo));
            String linea;
            while ((linea = br.readLine()) != null) {
                contenido.append(linea).append("\n");
            }
            br.close();
        } catch (IOException e) {
            e.printStackTrace();
        }
        return contenido.toString();
    }
