    private void jButton2MousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton2MousePressed
        // TODO add your handling code here:
        String matricula = jTextField1.getText();
        String nombre = jTextField2.getText();
        int edad = (int) jSpinner1.getValue();
        String sexo = (String) jComboBox1.getSelectedItem();
        String estadoCivil = jList1.getSelectedValue();

        try {
            // Validación de jTextField1 y jTextField2
            if (!validarMatricula(matricula)) {
                throw new Exception("ERROR: La matricula debe tener 6 digitos enteros.");
            }

            if (!validarNombre(nombre)) {
                throw new Exception("ERROR: El nombre debe ser una cadena de caracteres.");
            }

            // Validación de la edad
            if (edad <= 0) {
                throw new Exception("ERROR: La edad debe ser mayor que 0.");
            }

            // Mostrar los datos en el JTextArea
            jTextArea1.setText("Matrícula: " + matricula + "\n");
            jTextArea1.append("Nombre: " + nombre + "\n");
            jTextArea1.append("Edad: " + edad + "\n");
            jTextArea1.append("Sexo: " + sexo + "\n");
            jTextArea1.append("Estado Civil: " + estadoCivil);
        } catch (Exception ex) {
            System.out.println(ex.getMessage());
        }
    }//GEN-LAST:event_jButton2MousePressed

